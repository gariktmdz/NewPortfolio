/* Filter Skills*/

function filterSkills(category) {
  const items = document.querySelectorAll('.skills-grid-item');
  const buttons = document.querySelectorAll('.filter-buttons button');

  buttons.forEach(button => button.classList.remove('skills-active-btn'));

  if (category === 'all') {
    items.forEach(item => item.classList.remove('hidden'));
    buttons[0].classList.add('skills-active-btn');
  } else {
    items.forEach(item => {
      if (item.classList.contains(category)) {
        item.classList.remove('hidden');
      } else {
        item.classList.add('hidden');
      }
    });

    document.querySelector(`button[onclick="filterSkills('${category}')"]`).classList.add('skills-active-btn');
  }
}


/* Carousel*/

let currentIndex = 0;

function updateIndicators() {
    const indicators = document.querySelectorAll('.indicator');
    indicators.forEach((indicator, index) => {
        indicator.classList.toggle('active', index === currentIndex);
    });
}

function moveCarousel(direction) {
    const carouselInner = document.querySelector('.carousel-inner');
    const items = document.querySelectorAll('.carousel-item');
    const totalItems = items.length;

    currentIndex = (currentIndex + direction + totalItems) % totalItems;

    const translateX = -currentIndex * 100;
    carouselInner.style.transform = `translateX(${translateX}%)`;
    updateIndicators();
}

function moveToSlide(index) {
    const carouselInner = document.querySelector('.carousel-inner');
    const totalItems = document.querySelectorAll('.carousel-item').length;

    currentIndex = (index + totalItems) % totalItems;

    const translateX = -currentIndex * 100;
    carouselInner.style.transform = `translateX(${translateX}%)`;
    updateIndicators();
}

updateIndicators();

